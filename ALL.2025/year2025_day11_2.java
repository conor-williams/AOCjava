	static Map<String, Long> countWaysToReach(String end) {
		Map<String, Long> counts = new HashMap<>();
		Map<String, Long> newCounts = new HashMap<>();

		// newCounts = { node: (1 if node == end else 0) for node in mp }
		for (String node : mp.keySet()) {
			newCounts.put(node, node.equals(end) ? 1L : 0L);
		}

		// while newCounts != counts:
		while (!newCounts.equals(counts)) {
			counts = new HashMap<>(newCounts);
			newCounts = new HashMap<>();

			for (String node : counts.keySet()) {
				if (node.equals(end)) {
					newCounts.put(node, 1L);
				} else {
					long sum = 0;
					for (String child : mp.get(node)) {
						sum += counts.get(child);
					}
					newCounts.put(node, sum);
				}
			}
		}
		return newCounts;
	}
